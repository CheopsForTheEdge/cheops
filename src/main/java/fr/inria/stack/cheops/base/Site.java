package fr.inria.stack.cheops.base;

public class Site {
    private String id;
    private String name;
    private String api;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getApi() {
        return api;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setApi(String api) {
        this.api = api;
    }
}
